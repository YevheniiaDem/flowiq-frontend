function localizeDetail(detail: FopGroupDetail): FopGroupDetail {
  const summary = SUMMARY_UK[detail.id];
  const content = DETAIL_UK[detail.id];
  if (!summary) return detail;

  return {
    ...detail,
    ...summary,
    ...content,
    name: summary.name ?? detail.name,
    taxes: detail.taxes.map((tax) => ({
      ...tax,
      name:
        tax.name === "Unified Tax"
          ? "Єдиний податок"
          : tax.name === "ESV"
            ? "ЄСВ"
            : tax.name === "VAT"
              ? "ПДВ"
              : tax.name === "Personal Income Tax"
                ? "Податок на доходи"
                : tax.name === "Military Tax"
                  ? "Військовий збір"
                  : tax.name === "Employee Taxes"
                    ? "Податки на працівників"
                    : tax.name,
      description: tax.description,
      paymentDeadline: tax.paymentDeadline
        .replace("Quarterly", "Щоквартально")
        .replace("Monthly", "Щомісяця")
        .replace("by the", "до"),
    })),
    incomeLimits: content?.incomeLimits ?? {
      ...detail.incomeLimits,
      annual: detail.incomeLimits.annual.replace("/ year", "/ рік").replace("No limit", "Без ліміту"),
      note: detail.incomeLimits.note
        .replace("Exceeding the annual limit", "Перевищення річного ліміту")
        .replace("Monitor cumulative income", "Слідкуйте за накопиченим доходом"),
    },
    popularKveds: detail.popularKveds.map((k) => ({
      code: k.code,
      name: KVED_UK[k.code]?.name ?? k.name,
    })),
  };
}

export const mockFopGroupDetails: FopGroupDetail[] = enDetails.map(localizeDetail);

export function getFopGroupBySlug(slug: string): FopGroupDetail | undefined {
  return mockFopGroupDetails.find((group) => group.slug === slug);
}
